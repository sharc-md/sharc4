    #----START of ECI.calculate_Fmat------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------   
    def calculate_Fmat( self, nucs, mol ):
        F = np.zeros((mol.nao, mol.nao))
        for g in nucs:
            for a in range(g.mol.natm):
                Z, R = g.mol.atom_charge(a) - g.Q[a], g.mol.atom_coord(a)
                mol.set_rinv_orig(R)
                F += Z*mol.intor('int1e_rinv')
        return F
